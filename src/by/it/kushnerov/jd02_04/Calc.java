package by.it.kushnerov.jd02_04;

public class Calc {

    int sumScalar(int a, int b){
        return a+b;
    }

    int subScalar(int a, int b){
        return a-b;
    }

    int mulScalar(int a, int b){
        return a*b;
    }

    double divScalar(int a, int b){
        return (double)a/b;
    }
}
